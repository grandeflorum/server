package com.grandeflorum.statistic.domain;

public class TimeTatolValue {
    /**
     * 总价
     */
    private double zj;

    /**
     * 套数
     */
    private double ts;

    /**
     * 面积
     */
    private double mj;


    /**
     * 类型
     */
    private Integer lx;




    public double getZj() {
        return zj;
    }

    public void setZj(double zj) {
        this.zj = zj;
    }

    public double getTs() {
        return ts;
    }

    public void setTs(double ts) {
        this.ts = ts;
    }

    public double getMj() {
        return mj;
    }

    public void setMj(double mj) {
        this.mj = mj;
    }

    public Integer getLx() {
        return lx;
    }

    public void setLx(Integer lx) {
        this.lx = lx;
    }
}
